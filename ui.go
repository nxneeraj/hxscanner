// showLoadingAnimation displays a loading animation during the scanning process
func showLoadingAnimation() {
	fmt.Print("Loading: [")
	for i := 0; i < 10; i++ {
		fmt.Print("=")
		time.Sleep(100 * time.Millisecond)
	}
	fmt.Println("] Done!")
}

