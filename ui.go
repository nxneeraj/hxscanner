// clearScreen clears the console screen for a neat UI experience
func clearScreen() {
	if os.Getenv("OS") == "Windows_NT" {
		// Windows
		fmt.Print("\x0c")
	} else {
		// Unix-based systems
		fmt.Print("\033[H\033[2J")
	}
}

